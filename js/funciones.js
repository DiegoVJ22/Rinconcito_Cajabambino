const btnLeft = document.querySelector(".btn-left"),
      btnRight = document.querySelector(".btn-right"),
      btnLeft2 = document.querySelector(".btn-left2"),
      btnRight2 = document.querySelector(".btn-right2"),
      slider = document.querySelector("#slider"),
      slider2 = document.querySelector("#slider2"),
      sliderSection = document.querySelectorAll(".slider-section-plato"),
      sliderSection2 = document.querySelectorAll(".slider-section-cliente");


btnLeft.addEventListener("click", e => moveToLeft())
btnRight.addEventListener("click", e => moveToRight())
btnLeft2.addEventListener("click", e => moveToLeft2())
btnRight2.addEventListener("click", e => moveToRight2())

setInterval(() => {
    moveToRight2()
}, 5000);


let operacion = 0,
    counter = 0,
    widthImg = 100 / sliderSection.length;


let operacion2 = 0,
    counter2 = 0,
    widthImg2 = 100 / sliderSection2.length;

function moveToRight() {
    if (counter >= sliderSection.length-1) {
        counter = 0;
        operacion = 0;
        slider.style.transform = `translate(-${operacion}%)`;
        slider.style.transition = "none";
        return;
    } 
    counter++;
    operacion = operacion + widthImg;
    slider.style.transform = `translate(-${operacion}%)`;
    slider.style.transition = "all ease .6s"
    
}  

function moveToLeft() {
    counter--;
    if (counter < 0 ) {
        counter = sliderSection.length-1;
        operacion = widthImg * (sliderSection.length-1)
        slider.style.transform = `translate(-${operacion}%)`;
        slider.style.transition = "none";
        return;
    } 
    operacion = operacion - widthImg;
    slider.style.transform = `translate(-${operacion}%)`;
    slider.style.transition = "all ease .6s"
    
    
}

